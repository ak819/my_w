     */
    public function update(Request $request, $id)
    {
        $validatedData = $request->validate([
            'Value' => 'required',
        ],[
            'Value.required' => 'The currency value field is required',
        ]);
        $Currency=Currency::find($id);
        $Currency->Value=$request->input('Value');
        $Currency->IsEnable=($request->input('IsEnable')) ? 1 : 0;
        $Currency->save();
        return redirect()->route('currency.edit', $id)->withInput()->with('success', 'Currency has been editted successfully.');
    }

